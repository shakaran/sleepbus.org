    <p>Dear sleepbus Family,</p>

    <p>Here are some stats;</p>

    <p>- Sleepbus reached its first $20,000 goal in only 7 days (on March 4). Funds raised were used to purchase the first ever Sleepbus.</p>

    <p>- Sleepbus reached its second $50,000 goal in 47 days (on April 13). Funds raised are being used to pay for the fit-out of the bus.</p>

    <p>- Sleepbus reached its third $85,000 goal in 63 days (on April 29). Funds raised will be used to to cover part of the operating costs for the 90 day pilot program to test the sleepbus operational procedures and gain feedback from guests.</p>

    <p>I am amazed at the support received so far, this coming from a guy that has just 78 Facebook friends. Hahaha. It just goes to show what is truly possible if we come together. The people and businesses that have contacted me over the past 63 days have been so moved by sleepbus and so compelled to help; it is this help and support that has made our first bus possible.</p>

    <p>It truly will be the first bus of many, of this I have no doubt.</p>

    <p>I could never have imagined that this idea would resonate so strongly with you all, but I&#39;m so proud and happy that it has. This bus will change lives. That&rsquo;s so powerful. Nothing seems impossible with the support of the best family a little charity could ever hope for.</p>

    <p>Money that is not used for the pilot program will go towards our second bus, which we have waiting to be purchased. This bus and a third will be used for our second phase pilot program in Melbourne CBD. 100% of public donations go to sleepbus projects. I rely on corporate and future government support to fund the &quot;business&quot; of running sleepbus. 3 buses on the road will provide 24,090 safe sleeps per year...now that&rsquo;s a big number.</p>

    <p>Thank you again for your support. Couldn&#39;t do it without you.</p>

    <p>Simon</p>


